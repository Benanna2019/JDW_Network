<div class="banner">
  <h1>Hello and welcome to the JDW Network</h1>
  <h2>What is the JDW Network?</h2>

  <p>
    The JDW Network is a faith-based, family 501c (3), non-profit grassroots, resource mobilization vehicle, 
    committed to working together and overseeing projects/programs that make a difference in the lives of individuals, 
    families, small non-profit organizations, and “low-wealth” communities.  
  </p>
  <p>
    The JDW Network develops capacity-building networks and spiritual, economic, and social resources 
    that will address issues of economic and social deprivation, low commitment to schools, academic failure, 
    low neighborhood attachment, and community disorganization; and encourage creativity, as we strive to make a meaningful 
    impact on the lives of disenfranchised people.
  </p>


  <h3>Our Mission</h3>

  <p>
    The mission of the JDW Network is to provide spiritual inspiration, 
    tangible human and financial resources, and opportunities for those interested 
    in moral and social change within the 7.3 square miles, nineteen neighborhood 
    Enterprise Renewal Community (ERC) of Charleston, South Carolina, and contiguous counties.
  </p>

  <h3>Our Purpose</h3>
  <p>
    The overarching purpose of the JDW Network is to solve systemic spiritual, economic and social problems, 
    through spiritual resources and social and economic networking to augment the development of 
    spiritual (moral), human, economic, and social resources within the approximately eight (8) square mile 
    Peninsula Charleston Community by coordination national, regional, and local resources. 
  </p>
  <p>
    As best as possible, we’d like to plead our case through this webpage.  The purpose and mission of the JDW Network, 
    through local and national resource mobilization (or creative resourcing), is to provide stimulation and occasion for 
    those interested in common societal change.  In addition, we provide inspiration, resources, and opportunities for the 
    less fortunate who are interested in social change throughout the Peninsula, 
    specifically the 1.4 square-mile Eastside neighborhood.
  </p>
  <p>
    In this particular instance, the JDW Network will assist fragile families; specifically pregnant teenagers in Charleston, 
    expressly those residing on the Eastside/Westside of Charleston, SC, and contiguous communities. 
    We endeavor to facilitate in making biblically and spiritually them become better able to compete 
    culturally, academically, and in the job market by assisting them with 
    moral, employability and interpersonal/social/ emotional skills training.  
    Through the aforementioned effort, we will encourage community participation in the planning process, 
    fostering cooperation among the various constituencies in East Charleston, 
    and strengthening Charleston’s poorest communities as viable residential neighborhoods.
  </p>
  <p>
    We are currently in the process of developing the Low country HELPS Ministries, 
    and in the Peninsula Biblical Counseling Ministries (PBCM). 
  </p>
  <p>
    The JDW Network will write grants, perform product, and process evaluation, and do client needs assessments for 
    Charleston non-profit/faith-based organizations. usually takes a day to complete and involves a series of individual 
    and group interviews. 
  </p>
  <p>
    The JDW Network will, in some cases, perform a comprehensive on-site assessment, and will augment 
    these assessments with monitoring visits and a review of your existing promotional materials including 
    mission statement, strategic plan, annual report, as well as previously submitted grant applications.
  </p>
